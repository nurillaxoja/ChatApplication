namespace An.World.Animation
{
    /// <summary>
    /// Styles for page animations for appearing/dissapearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No any animation
        /// </summary>
        None = 0,
        /// <summary>
        /// Page slides in and fades in from the right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// Page slides out and fades out to the left
        /// </summary>
        SlideAndFadeOutToLeft = 2,

    }
}
